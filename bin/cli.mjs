#!/usr/bin/env node
// claude-rules — install shared Claude Code rules / agents / kit into a repo.
// shadcn-style: copy + own + pin. The CLI is deliberately dumb; the source of
// truth is registry.json. It NEVER merges build config (lefthook/eslint) — kit
// entries are scaffolded and their wiring is printed for you to do once.
//
// Usage:
//   npx github:dohrm/claude-rules add rust [ts go] [--ref v1.2.0]
//   npx github:dohrm/claude-rules update [--ref v1.3.0]
//   npx github:dohrm/claude-rules init      # assemble justfile + lefthook.yml (if absent)
//   npx github:dohrm/claude-rules list
import { readFileSync, writeFileSync, existsSync, copyFileSync } from 'node:fs'
import { spawnSync } from 'node:child_process'
// giget is imported lazily (only add/update need it) so init/list run with no deps.

const registry = JSON.parse(readFileSync(new URL('../registry.json', import.meta.url), 'utf8'))
const LOCK = '.claude-rules.lock'

const argv = process.argv.slice(2)
const cmd = argv[0]
const refFlag = (() => { const i = argv.indexOf('--ref'); return i >= 0 ? argv[i + 1] : null })()
const positional = argv.slice(1).filter(a => a !== '--ref' && a !== refFlag)

function readLock() {
  return existsSync(LOCK) ? JSON.parse(readFileSync(LOCK, 'utf8')) : null
}
function writeLock(ref, profiles) {
  writeFileSync(LOCK, JSON.stringify({ repo: registry.repo, ref, profiles }, null, 2) + '\n')
}

// Layout-agnostic globs per tech (the path lives in the justfile *_dir vars).
const GLOB = { rust: '**/*.rs', ts: '**/*.{ts,tsx}', go: '**/*.go' }

function genLefthook(techs) {
  const cmds = suffix => techs.map(t =>
    `    ${t}:\n      glob: "${GLOB[t]}"\n      run: just ${t}-${suffix}`).join('\n')
  return `# Generated by \`claude-rules init\` — thin triggers → justfile recipes.\n`
    + `# Commands and their paths live in the justfile (\`just <tech>-lint\`/\`-check\`).\n\n`
    + `pre-commit:\n  parallel: true\n  commands:\n${cmds('lint')}\n\n`
    + `pre-push:\n  parallel: true\n  commands:\n${cmds('check')}\n`
}

function initRepo() {
  const lock = readLock()
  if (!lock) { console.error(`No ${LOCK} — run "add <profile...>" first.`); process.exit(1) }
  const techs = lock.profiles.filter(p => GLOB[p])

  const snippet = '.claude/kit/common/justfile.snippet'
  if (existsSync('justfile') || existsSync('Justfile'))
    console.log(`• justfile exists — merge ${snippet} into it, then set the *_dir variables.`)
  else if (existsSync(snippet)) { copyFileSync(snippet, 'justfile'); console.log(`✓ created justfile (from ${snippet}) — set the *_dir variables to your layout.`) }
  else console.log(`• ${snippet} missing — run "add" first.`)

  if (existsSync('lefthook.yml') || existsSync('lefthook.yaml'))
    console.log(`• lefthook.yml exists — merge .claude/kit/<tech>/lefthook.snippet.yml (thin triggers) into it.`)
  else if (techs.length) { writeFileSync('lefthook.yml', genLefthook(techs)); console.log(`✓ created lefthook.yml (triggers for: ${techs.join(', ')}).`) }

  if (!existsSync('.git')) console.log('• not a git repo — run `lefthook install` after `git init`.')
  else {
    const r = spawnSync('lefthook', ['install'], { stdio: 'inherit' })
    if (r.error) console.log('• lefthook not found — install it, then run: lefthook install')
  }

  console.log(`\nStill manual (repo-specific): move deny.toml→<rust_dir>, mutants.toml→<rust_dir>/.cargo/, golangci.base.yml→.golangci.yml, mutation-ci.yaml→.gitea/workflows/; adapt eslint globalIgnores; enable your techs in the justfile \`check\` recipe.`)
}

async function fetchEntry(ref, entry) {
  const { downloadTemplate } = await import('giget')   // cached after first call
  const src = `github:${registry.repo}/${entry.from}#${ref}`
  await downloadTemplate(src, { dir: entry.to, force: true })
  console.log(`  ✓ ${entry.from}  →  ${entry.to}`)
  return entry.wire ? `  • ${entry.to}: ${entry.wire}` : null
}

async function install(profiles, ref) {
  const unknown = profiles.filter(p => !registry.profiles[p])
  if (unknown.length) {
    console.error(`Unknown profile(s): ${unknown.join(', ')}. Available: ${Object.keys(registry.profiles).join(', ')}`)
    process.exit(1)
  }
  const entries = [...registry.shared, ...profiles.flatMap(p => registry.profiles[p])]
  console.log(`Installing [${profiles.join(', ')}] from ${registry.repo}#${ref}\n`)
  const notes = []
  for (const e of entries) {
    const note = await fetchEntry(ref, e)
    if (note) notes.push(note)
  }
  writeLock(ref, profiles)
  console.log(`\nPinned in ${LOCK} (ref ${ref}).`)
  if (notes.length) {
    console.log(`\nOne-time wiring (the installer never touches your build config):`)
    console.log(notes.join('\n'))
  }
  console.log(`\nRules in .claude/rules/ auto-load — no @import needed (language rules are path-scoped via their \`paths:\` frontmatter). Agents in .claude/agents/ and skills in .claude/skills/ are auto-discovered.`)
}

async function main() {
  switch (cmd) {
    case 'add': {
      if (!positional.length) { console.error('Usage: add <profile...> [--ref <ref>]'); process.exit(1) }
      await install(positional, refFlag || registry.defaultRef)
      break
    }
    case 'update': {
      const lock = readLock()
      if (!lock) { console.error(`No ${LOCK} found — run "add <profile...>" first.`); process.exit(1) }
      await install(lock.profiles, refFlag || registry.defaultRef)
      break
    }
    case 'init': {
      initRepo()
      break
    }
    case 'list': {
      const lock = readLock()
      console.log('Available profiles:')
      for (const [name, entries] of Object.entries(registry.profiles)) {
        console.log(`  ${name}  (${entries.map(e => e.from).join(', ')})`)
      }
      console.log(lock ? `\nInstalled: [${lock.profiles.join(', ')}] @ ${lock.ref}` : '\nInstalled: none')
      break
    }
    default:
      console.log('claude-rules — usage:\n  add <profile...> [--ref <ref>]   install/pin profiles\n  update [--ref <ref>]             re-install locked profiles at ref\n  init                             assemble justfile + lefthook.yml (if absent) + lefthook install\n  list                             show available & installed profiles')
  }
}

main().catch(err => { console.error(err.message || err); process.exit(1) })
